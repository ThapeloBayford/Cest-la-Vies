
namespace GameLogic
{
	public class Asset {
	private int value;
	private int appreciation;
	private int depreciation;
	private int expenses;

	public int getValue(){
		return value;
	}

	public int getAppreciation(){
		return appreciation;
	}

	public int getDepreciation(){
		return depreciation;
	}

	public int getExpenses(){
		return expenses;
	}
}

}
